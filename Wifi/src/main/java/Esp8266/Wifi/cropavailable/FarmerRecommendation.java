package Esp8266.Wifi.cropavailable;

public class FarmerRecommendation {

	public int farmer_id;
	public int recommend_crop_id;
	public int primary_crop_id;
	public int getFarmer_id() {
		return farmer_id;
	}
	public void setFarmer_id(int farmer_id) {
		this.farmer_id = farmer_id;
	}
	public int getRecommend_crop_id() {
		return recommend_crop_id;
	}
	public void setRecommend_crop_id(int recommend_crop_id) {
		this.recommend_crop_id = recommend_crop_id;
	}
	public int getPrimary_crop_id() {
		return primary_crop_id;
	}
	public void setPrimary_crop_id(int primary_crop_id) {
		this.primary_crop_id = primary_crop_id;
	}
	
	
}
